/*
|-----------------------------------------------------------------------------------------------------------------------
|   Remove the main WYSIWYG editor.
|-----------------------------------------------------------------------------------------------------------------------
*/
function tvds_remove_support_init(){
    remove_post_type_support( 'page', 'editor');
    remove_post_type_support( 'portfolio', 'editor');
}
add_action('init', 'tvds_remove_support_init', 100);

